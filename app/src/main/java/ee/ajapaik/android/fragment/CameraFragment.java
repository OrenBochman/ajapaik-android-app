    @Override
    public void onActivityCreated(Bundle savedInstanceState) {
        Log.d(TAG, "onActivityCreated");

        super.onActivityCreated(savedInstanceState);

        if (savedInstanceState != null) {
            m_flippedMode = savedInstanceState.getBoolean(KEY_FLIPPED_MODE);
            m_opacity = savedInstanceState.getFloat(KEY_OPACITY, DEFAULT_OPACITY);
            m_photo = savedInstanceState.getParcelable(KEY_PHOTO);
            m_scale = savedInstanceState.getFloat(KEY_SCALE, DEFAULT_SCALE);
        }

        if (m_photo == null) {
            m_photo = getPhoto();
        }

        getImageView().setFlipped(m_flippedMode);
        getImageView().setScale(m_scale);
        getImageView().setImageURI(m_photo.getThumbnail(THUMBNAIL_SIZE));
        getImageView().setAlpha(0.5F);
        getActivity().setRequestedOrientation(
                m_photo.isLandscape()
                        ? ActivityInfo.SCREEN_ORIENTATION_LANDSCAPE
                        : ActivityInfo.SCREEN_ORIENTATION_PORTRAIT
        );

        getImageView().setOnLoadListener(imageLoadListener());
        getImageView().setOnTouchListener(new OnCompositeTouchListener(new View.OnTouchListener[]{

                new OnScaleTouchListener(getActivity()) {
                    @Override
                    public void onScale(float scale) {
                        WebImageView imageView = getImageView();

                        imageView.setScale(imageView.getScale() * scale);
                        m_scale = imageView.getScale();
                    }
                },

                new OnCompositeTouchListener() {

                    @Override
                    public boolean onTouch(View v, MotionEvent event) {
                        return changeOpacity(v, event);
                    }

                    static final int NONE = 0;
                    static final int DRAG = 1;
                    int mode = NONE;

                    PointF last = new PointF();
                    PointF start = new PointF();

                    private float alpha = 0.5f;
                    private float newAlpha = 0.5f;

                    private boolean changeOpacity(View v, MotionEvent event) {
                        switch (event.getAction()) {
                            case MotionEvent.ACTION_DOWN:
                                last.set(event.getX(), event.getY());
                                start.set(last);
                                mode = DRAG;
                                break;
                            case MotionEvent.ACTION_MOVE:
                                if (mode == DRAG) {
                                    getNewOpacity(v, event);
                                    getImageView().setAlpha(newAlpha);
                                }
                                break;

                            case MotionEvent.ACTION_UP:
                                mode = NONE;
                                alpha = newAlpha;
                                break;

                            case MotionEvent.ACTION_POINTER_UP:
                                mode = NONE;
                                break;
                        }
                        return true;
                    }

                    private void getNewOpacity(View v, MotionEvent event) {
                        float startVal;
                        float curVal;
                        if (m_photo.isLandscape()) {
                            startVal = (start.x / (float) (v.getWidth()));
                            curVal = (event.getX() / (float) (v.getWidth()));
                        } else {
                            startVal = (start.y / (float) (v.getHeight()));
                            curVal = (event.getY() / (float) (v.getHeight()));
                        }
                        float diff = curVal - startVal;
                        newAlpha = alpha + diff;
                        newAlpha = clamp(newAlpha, 0.0f, 1.0f);
                    }

                    private float clamp(float value, float min, float max) {
                        return Math.min(Math.max(min, value), max);
                    }
                },

                new OnSwipeTouchListener(getActivity()) {
                    @Override
                    public void onSingleTap() {
                        getImageView().setVisibility(INVISIBLE);
                    }

                    @Override
                    public void onLongHold(MotionEvent e) {
                        if (e.getPointerCount() == 1) {
                            takePicture();
                        }
                    }
                }
        }));
        getMainLayout().setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                getImageView().setVisibility(getImageView().getVisibility() == VISIBLE ? INVISIBLE : VISIBLE);
            }
        });

        getMainLayout().setOnLongClickListener(new View.OnLongClickListener() {
            @Override
            public boolean onLongClick(View view) {
                takePicture();
                return true;
            }
        });
    }

